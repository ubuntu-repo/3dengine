#ifndef DEFS_H
#define DEFS_H

///////////////////////////////////////////////////////////////////////////////
// Define two macros to return the max and min between two values
///////////////////////////////////////////////////////////////////////////////
#define MAX(x,y) ((x) >= (y)) ? (x) : (y)
#define MIN(x,y) ((x) <= (y)) ? (x) : (y)

///////////////////////////////////////////////////////////////////////////////
// Define constants for FPS and game loop frame time
///////////////////////////////////////////////////////////////////////////////
#define FPS 30
#define FRAME_TARGET_TIME (1000 / FPS)

///////////////////////////////////////////////////////////////////////////////
// Declare a new enumeration type for Boolean variables
///////////////////////////////////////////////////////////////////////////////
typedef enum { FALSE, TRUE } bool;

///////////////////////////////////////////////////////////////////////////////
// Struct declaration for Vectors and Matrices
///////////////////////////////////////////////////////////////////////////////
typedef struct {
    float x;
    float y;
} vec2d;

typedef struct {
    float x;
    float y;
    float z;
} vec3d;

typedef struct {
    int a;
    int b;
    int c;
    int face_index;
    uint32_t color;
} triangle;

typedef struct {
    float m[4][4];
} mat4x4;

#endif
