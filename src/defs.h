#ifndef DEFS_H
#define DEFS_H

///////////////////////////////////////////////////////////////////////////////
// Define constants for FPS and game loop frame time
///////////////////////////////////////////////////////////////////////////////
#define FPS 30
#define FRAME_TARGET_TIME (1000 / FPS)

///////////////////////////////////////////////////////////////////////////////
// Type definition for 2D and 3D Vectors
///////////////////////////////////////////////////////////////////////////////
typedef struct {
    float x;
    float y;
} vec2d;

typedef struct {
    float u;
    float v;
    float w;
} tex2d;

typedef struct {
    float x;
    float y;
    float z;
} vec3d;

///////////////////////////////////////////////////////////////////////////////
// Type definition for Triangles
///////////////////////////////////////////////////////////////////////////////
typedef struct {
    int a;
    int b;
    int c;
    uint32_t color;
} triangle;

///////////////////////////////////////////////////////////////////////////////
// Type definition for 4x4 Matrices
///////////////////////////////////////////////////////////////////////////////
typedef struct {
    float m[4][4];
} mat4x4;

#endif
